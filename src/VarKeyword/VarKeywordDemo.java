package VarKeyword;

public class VarKeywordDemo {
    public static void main(String[] args) {
        var name = "CodeSnippet";
        var count = 0;
        for (int i = 1; i < 5; i++) {
            ++count;
        }
        System.out.println("Count: " + count);
        System.out.println(name);
    }
}
